public class Main {
    public static void main(String[] args) {
        // for 
        for (int i=1; i<5; i++) {   
            System.out.println("for döngüsü : " + i);
        }

        // while 
        int j = 0;
        while (j<5){
            System.out.println("while döngüsü : " + j);
            j++;
        }

        // do-while 
        int k = 1;
        do {
            System.out.println("do-while döngüsü : " + k);
            k++;
        } while (k<5);

    }
}
